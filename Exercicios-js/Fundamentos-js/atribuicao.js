let v_preco = 2;

// Soma
v_preco += 3;
console.log(v_preco); // resultado 5
//Subtracao
v_preco -= 2;
console.log(v_preco); // resultado 3
//Multiplicacao
v_preco *= 2;
console.log(v_preco); // resultado 6
//divisao
v_preco /= 2;
console.log(v_preco); // resultado 3
// Modular
v_preco %= 5;
console.log(v_preco); // resultado 3
