let index = 0

// Usando la'co for
for (index = 0; index < 20; index++) {
    console.log("Valor-FOR:" + index);
}

//usando laco while
while (index > 1) {
    index--;
    console.log("Valor-While: " + index);
}

//Usando laco DO
do {
    console.log("Valor-DO: " + index++);

} while (index <= 15);

